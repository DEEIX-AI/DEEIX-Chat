import { cpSync, mkdirSync, readdirSync, rmSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const rootDir = dirname(dirname(fileURLToPath(import.meta.url)));
const sourceDir = join(rootDir, "node_modules", "@lobehub", "icons-static-svg", "icons");
const targetDir = join(rootDir, "public", "vendor", "lobehub-icons");
const manifestDir = join(rootDir, "shared", "generated");
const manifestFile = join(manifestDir, "lobehub-icon-manifest.ts");

function titleFromSlug(slug) {
  return slug
    .replace(/-(brand|brand-color|color|text|text-cn)$/u, "")
    .split("-")
    .filter(Boolean)
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join(" ");
}

rmSync(targetDir, { force: true, recursive: true });
mkdirSync(targetDir, { recursive: true });
cpSync(sourceDir, targetDir, { recursive: true });

const icons = readdirSync(targetDir)
  .filter((name) => name.endsWith(".svg"))
  .sort((left, right) => left.localeCompare(right))
  .map((fileName) => {
    const slug = fileName.slice(0, -4);
    return {
      id: slug,
      name: titleFromSlug(slug),
      src: `/vendor/lobehub-icons/${fileName}`,
    };
  });

mkdirSync(manifestDir, { recursive: true });
writeFileSync(
  manifestFile,
  [
    "// This file is generated by scripts/sync-lobehub-icons.mjs.",
    `export const lobehubIconManifest = ${JSON.stringify(icons, null, 2)} as const;\n`,
  ].join("\n"),
);
